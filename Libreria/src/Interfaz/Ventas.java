    private void CedulaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_CedulaFocusLost
        String cedula = Cedula.getText();
        if (!cedula.equals("")) {
            for (Cliente cliente : CLIENTES) {
                if(cliente.getCedula().equals(cedula)){
                    cli = cliente;
                    Nombre.setText(cli.getNombre());
                    Apellido1.setText(cli.getApellido1());
                    Apellido2.setText(cli.getApellido2());
                    Telefono.setText(String.valueOf(cli.getTelefono()));
                    break;
                }
            }
        }
    }//GEN-LAST:event_CedulaFocusLost

    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
        if (Herramientas.ValidarCampos(new Component[]{Cedula,Nombre,Apellido1,Apellido2,Telefono})) {
            if (cli.getCedula().equals("000000000")) {
                cli = new Cliente(Cedula.getText(),Nombre.getText(),Apellido1.getText(),Apellido2.getText(),Integer.parseInt(Telefono.getText()));
                factura.setClient(cli);
            }else
                factura.setClient(cli);
            ocultar();
        } else 
            Herramientas.Error("No has llenado todos los datos", "Ingrese los datos solicitados");
    }//GEN-LAST:event_AceptarActionPerformed

