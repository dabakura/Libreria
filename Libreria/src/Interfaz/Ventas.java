    private void IniciarHilos() {
        DateFormat hora = new SimpleDateFormat("hh:mm:ss aa");
        new Thread(){
            @Override
            public void run() {
                do {
                    if (Util.getClienteDefault()!=null) {
                        cli = Util.getClienteDefault();
                        break;
                    } else {
                        try {
                            sleep(100);
                        } catch (InterruptedException e) {
                            break;
                        }
                    }
                } while (true);
                factura = Util.ObtenerFactura();
                factura.setClient(cli);
                do {
                    try {
                        Impuesto.setText(""+factura.getIV());
                        Subtotal.setText(""+factura.getSubtotal());
                        Total.setText(""+factura.getTotal());
                        Hora.setText(hora.format(new Date()));
                        sleep(500);
                    } catch (InterruptedException ex) {}
                } while (true);
            }
        }.start();
    }
