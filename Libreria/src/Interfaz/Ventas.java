    private void MostrarFactura(Factura factura){
        DateFormat  dia,hora;
        dia = new SimpleDateFormat("dd/MM/yyyy");
        hora = new SimpleDateFormat("hh:mm:ssaa");
        String mensaje = "Recibo No:\t"+factura.getCod_Factura()+
                "\nFecha: "+dia.format(factura.getFecha())+"\tHora: "+hora.format(factura.getFecha())+
                "\nCliente:\n"+factura.getClient().getNombre()+" "+factura.getClient().getApellido1()+" "+factura.getClient().getApellido2()+
                "\nCedula:\t"+factura.getClient().getCedula()+
                "\n\n---------------------------------------"+
                "\n          Detalle del Recibo"+
                "\n---------------------------------------"+
                "\nCant Libro\t\t\t Monto\n";
        
        for (Detalle detalle : factura.getDetalles()) {
            char[] cadena = "                           ".toCharArray();
            int largo = (detalle.getLibro().getTitulo().length()<27)?detalle.getLibro().getTitulo().length():27;
            String uno = detalle.getLibro().getTitulo().substring(0, largo);
            System.arraycopy(uno.toCharArray(), 0, cadena, 0, uno.toCharArray().length);
            mensaje+= detalle.getUnidades()+"  "+String.valueOf(cadena)+"  ₡"+detalle.getTotal()+"\n";
        }
        mensaje+="\n---------------------------------------"+
                "\nMontos:"+
                "\n\tSubTotal:\t\t₡"+factura.getSubtotal()+
                "\n\tImpuesto:\t\t₡"+factura.getIV()+
                "\n\tTotal:   \t\t₡"+factura.getTotal()+
                "\n---------------------------------------"+
                "\n---------------------------------------"+
                "\nForma de Pago:"+
                "\n\tEfectivo:\t\t₡"+pago+
                "\n\tVuelto:  \t\t₡"+(pago-factura.getTotal())+
                "\n---------------------------------------\n"+
                "\n      Para reclamos o devoluciones\n      debe presentarse la factura\n      Muchas gracias por su compra";
        int renglones = mensaje.split("\n").length;
        int i = 0;
        for (String s: mensaje.split("\n")) {
            if (i<s.length()) {
                i=s.length();
            }
        }
        i+=6;
        area.setRows(renglones);
        area.setColumns(i);
        area.setText(mensaje);
        jDialogFactura.setMinimumSize(new Dimension(350, renglones*19+170));
        jDialogFactura.setVisible(true);
    }
    private void ActualizaTabla(){
        DefaultTableModel modelo = LimpiarTabla();
        factura.getDetalles().forEach((d) -> {
            modelo.addRow(new String[]{d.getLibro().getTitulo(),""+d.getCosto_Unitario(),""+d.getUnidades(),""+d.getTotal()});
        });
        Detalle.setModel(modelo);
    }
    
    private DefaultTableModel LimpiarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) Detalle.getModel();
        int filas = modelo.getRowCount();
        for (int i = 0; i < filas; i++) {
            modelo.removeRow(0);
        }
        Detalle.setModel(modelo);
        return modelo;
    }
    
