    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        if (!ISBN.getText().equals("")) {
            Libro l = Util.getLibro(ISBN.getText());
            if (l!=null) {
                if (l.getEjemplares().facturable(Integer.valueOf(Cantidad.getValue().toString()))) {
                    factura.addDetalle(new Detalle(factura.getCod_Factura(), l, Integer.valueOf(Cantidad.getValue().toString())));
                    ActualizaTabla();
                }else   
                    Herramientas.Error("No se dispone la cantidad solicitada\nsolo se pueden vender "+l.getEjemplares().getCantidad_Almacenada(), "Faltan existencias");
            }else{
                Herramientas.Error("Este libro no se encuentra registrado", "Libro no existe");
            }
        } else 
            Herramientas.Error("Ingrese un ISBN y seleccione la cantidad deseada", "Faltan datos");
    }//GEN-LAST:event_AgregarActionPerformed

