    private void jLabel2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MousePressed
        rutaarchivo.setFileFilter(new FileNameExtensionFilter("Imagenes", "jpg","png","gif","jpeg","JPG"));
        int estado = rutaarchivo.showDialog(null, "Abrir Archivo");
        if(estado==JFileChooser.APPROVE_OPTION){
            archivo = rutaarchivo.getSelectedFile();
            if(archivo.canRead()){
                if (archivo.getName().endsWith(".jpg")||
                        archivo.getName().endsWith(".png")||
                        archivo.getName().endsWith(".gif")||
                        archivo.getName().endsWith(".jpeg")||
                        archivo.getName().endsWith(".JPG")){
                    byteImg = vgestion.abrirImagen(archivo);
                    ImageIcon icon = new ImageIcon(byteImg);
                    Imagen = icon;
                    icon = new ImageIcon(icon.getImage().getScaledInstance(250, 360, 0));
                    jLabel2.setIcon(icon);
                } else
                    Herramientas.Error("Solo se permiten archivos Imagen.", "Formato no permitido");
            } else 
                Herramientas.Error("Error al intentar cargar el archivo", "Archivo corrupto");
        } else if (estado!=JFileChooser.CANCEL_OPTION) {
            Herramientas.Error("Buscador de archivos no funciona", "Lo sentimos");
        }
    }//GEN-LAST:event_jLabel2MousePressed

    private void VerdeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VerdeActionPerformed
        Libro nuevo = Datos.getLibro();
        if (nuevo!=null) {
            if (Autores.size()>0) {
                nuevo.setListaAutores(Autores);
                if (Temas.size()>0){
                    nuevo.setListaTemas(Temas);
                    if (!Editorial.getItemAt(Editorial.getSelectedIndex()).equals("") &&
                            !Editorial.getItemAt(Editorial.getSelectedIndex()).equals("Seleccionar")) {
                        Editorial e = EDITORIALES.get(Editorial.getSelectedIndex());
                        nuevo.setEditorial(new Editorial(e));
                        if (!Categoria.getItemAt(Categoria.getSelectedIndex()).equals("") &&
                                !Categoria.getItemAt(Categoria.getSelectedIndex()).equals("Seleccionar")) {
                            Categoria c = CATEGORIAS.get(Categoria.getSelectedIndex());
                            nuevo.setCategoria(new Categoria(c));
                            String ruta = "/src/Image/";
                            if (Imagen!=null) {
                                nuevo.setImagen(Imagen);
                            }else if (Modificar){
                                nuevo.setImagen(LibroModificable.getImagen());
                            }else{
                                File file = new File("./"+ruta+"foto.jpg");
                                byteImg = vgestion.abrirImagen(file);
                                ImageIcon icon = new ImageIcon(byteImg);
                                Imagen = icon;
                                nuevo.setImagen(Imagen);
                            }
                            ruta += nuevo.getIbsn()+".png";
                            File file = new File("./" + ruta);
                            if (Modificar) {
                                if (Imagen!=null) {
                                    file.delete();
                                    file = new File("./" + ruta);
                                    vgestion.guardarImagen(file, byteImg);
                                }
                                Util.ActualizaLibro(nuevo);
                                Herramientas.mensage("Has modificado el Libro", "Modificacion de Libro Exitosa");
                            }else{
                                vgestion.guardarImagen(file, byteImg);
                                LIBROS.add(nuevo);
                                BaseDatosControlador.GUARDARSTOCK(nuevo.getEjemplares());
                                BaseDatosControlador.GUARDARLIBRO(nuevo);
                                Herramientas.mensage("Has ingresado el Libro", "Ingreso de Libro Exitoso");
                            }
                        } else Herramientas.Error("No has seleccionado la Categoria", "Especifique la Categoria");
                    } else Herramientas.Error("No has seleccionado la Editorial", "Especifique la Editorial");
                } else Herramientas.Error("No has ingresado los Tema(s)", "Especifique el Tema");
            }
            else Herramientas.Error("No has ingresado el Autor(es)", "Especifique el Autor");
        }
    }//GEN-LAST:event_VerdeActionPerformed

