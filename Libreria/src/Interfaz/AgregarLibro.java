    private void cargarAutoresLista(){
        int[] array = jList1.getSelectedIndices();
        AutorSelec.clear();
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        int x = table.getRowCount();
        for (int i = 0; i < x; i++) {
            table.removeRow(0);
        }
        for (int i: array) {
            String[] s = AUTORES.get(i).getListaIngresados().split(",");
            AutorSelec.add(new Autor(AUTORES.get(i)));
            table.addRow(new Object[]{s[0],s[1],s[2],s[3]});
        }
    }
