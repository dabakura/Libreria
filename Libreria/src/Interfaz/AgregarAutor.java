    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (Herramientas.ValidarCampos(new Component[] {codautor,nombre,apellido,ano,observaciones})) {
            AUTORES.add(new Autor(codautor.getText(),nombre.getText(),apellido.getText(),Util.getPais(pais.getSelectedIndex()),Integer.parseInt(ano.getText()),observaciones.getText()));
            BaseDatosControlador.GUARDARAUTORES(AUTORES.getLast());
        } else { 
            Herramientas.Error("No has llenado todos los campos\nAsegurate de llenar bien la información", "Campos Vacios");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

