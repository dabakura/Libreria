    private void jLabel1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MousePressed
        rutaarchivo.setFileFilter(new FileNameExtensionFilter("Imagenes", "jpg","png","gif","jpeg","JPG"));
        int estado = rutaarchivo.showDialog(null, "Abrir Archivo");
        if(estado==JFileChooser.APPROVE_OPTION){
            archivo = rutaarchivo.getSelectedFile();
            if(archivo.canRead()){
                if (archivo.getName().endsWith(".jpg")||
                        archivo.getName().endsWith(".png")||
                        archivo.getName().endsWith(".gif")||
                        archivo.getName().endsWith(".jpeg")||
                        archivo.getName().endsWith(".JPG")){
                    byteImg = vgestion.abrirImagen(archivo);
                    ImageIcon icon = new ImageIcon(byteImg);
                    icon = new ImageIcon(icon.getImage().getScaledInstance(278, 318, 0));
                    jLabel1.setIcon(icon);
                } else
                    Herramientas.Error("Solo se permiten archivos Imagen.", "Formato no permitido");
            } else 
                Herramientas.Error("Error al intentar cargar el archivo", "Archivo corrupto");
        } else if (estado!=JFileChooser.CANCEL_OPTION) {
            Herramientas.Error("Buscador de archivos no funciona", "Lo sentimos");}
    }//GEN-LAST:event_jLabel1MousePressed

